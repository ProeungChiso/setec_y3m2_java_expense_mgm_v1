    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String inputStaffName = staffName.getText();
        String inputPosition = position.getText();
        String inputUsername = username.getText();
        String inputPassword = password.getText();
        String setDefaultRole = "user";

        System.out.println("Staff Name: " + inputStaffName);
        System.out.println("Staff Position: " + inputPosition);
        System.out.println("Staff Username: " + inputUsername);
        System.out.println("Staff Password: " + inputPassword);

        try {

            String sql = "INSERT INTO staff (s_name, position, username, password, role) VALUES (?,?,?,?,?)";

            Connection conn = DatabaseConfig.getConnection();

            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, inputStaffName);
            pst.setString(2, inputPosition);
            pst.setString(3, inputUsername);
            pst.setString(4, inputPassword);
            pst.setString(5, setDefaultRole);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "New Staff Created !");

            AdminForm adminForm = new AdminForm();
            adminForm.setVisible(true);
            dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database operation failed: " + e.getMessage(), "Create Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCreateActionPerformed

